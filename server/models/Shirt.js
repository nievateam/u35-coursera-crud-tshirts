const mongoose = require('mongoose')

const ShirtSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
})

const Shirt = mongoose.model('Shirt', ShirtSchema)

module.exports = Shirt
